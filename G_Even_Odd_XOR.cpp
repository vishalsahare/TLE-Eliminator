void solve() {

    int n; cin >> n;

    if (n == 3) {
        cout << "1 2 3";
        return;
    }

    stack<int> even, odd;
    int num = 2;
    for (int i = 0; i < n/2; i++) {
        even.push(num++);
        odd.push(num++);
    }

    if ((n/2) & 1) {
        int p = odd.top(); odd.pop();
        p ^= 1;

        int prependOne = 1LL << 20;
        p ^= prependOne;
        odd.top() ^= prependOne;
        odd.push(p);
    }

    for (int i = 1; i <= n; i++) {
        if ((n&1) and i == n) {
            cout << 0 << endl;
        } else if (i&1) {
            cout << odd.top() << " ";
            odd.pop();
        } else {
            cout << even.top() << " ";
            even.pop();
        }
    }
   
}



